public class TargetHUD {
   public float profiles$;
   public float shops$;
   public float bangkok$;
   public float building$;
   public float zambia$;
   public float literary$;
   public float elephant$;
   public float winter$;
   public Color sender$;

   public void _gilbert() {
      RoundedUtil._ticket(ugidacav.profiles$ + ugidacav.bangkok$, ugidacav.shops$ + ugidacav.building$, ugidacav.elephant$, ugidacav.elephant$, ugidacav.elephant$ / 2.0F - 0.5F, ColorUtil._resulted(ugidacav.sender$, ugidacav.winter$ / 255.0F));
   }

   public void _bradley() {
      for(Object memories = 1; memories <= 2; ++memories) {
         theta.bangkok$ += theta.zambia$;
         theta.building$ += theta.literary$;
         theta.literary$ = (float)((double)theta.literary$ * 0.97D);
         theta.zambia$ = (float)((double)theta.zambia$ * 0.97D);
         --theta.winter$;
         if (theta.winter$ < 1.0F) {
            theta.winter$ = 1.0F;
         }
      }

   }

   public void _boulder(float remibeve, float ufefigop, float pugafafo, float bovirigu, float emorobav, Color alilobog) {
      rusapita.profiles$ = (float)remibeve;
      rusapita.shops$ = (float)ufefigop;
      rusapita.zambia$ = (float)pugafafo;
      rusapita.literary$ = (float)bovirigu;
      rusapita.elephant$ = (float)emorobav;
      rusapita.winter$ = 254.0F;
      rusapita.sender$ = (Color)alilobog;
   }
}
