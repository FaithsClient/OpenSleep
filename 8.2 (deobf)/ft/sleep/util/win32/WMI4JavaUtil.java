package ft.sleep.util.win32;

public class WMI4JavaUtil {
   public static String _scared(String facing, Iterable overseas) {
      Object agenda = new StringBuilder();

      for(Object ridge : overseas) {
         agenda.append(ridge);
         agenda.append((String)facing);
      }

      agenda.delete(agenda.length() - ((String)facing).length(), agenda.length());
      return agenda.toString();
   }
}
